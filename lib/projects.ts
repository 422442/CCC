// Helper functions for filtering and finding projects
export const getFeaturedProjects = () => allProjects.filter((p) => p.featured)

export const getProjectBySlug = (slug: string) => allProjects.find((p) => p.slug === slug)

export const getProjectsByCategory = (category: string) =>
  category === "All" ? allProjects : allProjects.filter((p) => p.category === category)

export const getAllCategories = () => {
  const categories = Array.from(new Set(allProjects.map((p) => p.category)))
  return ["All", ...categories.sort()]
}
